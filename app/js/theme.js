const btnMuted = document.getElementById('btn-muted');


btnMuted.onclick = function () {
   const video = document.getElementById('video');

   if (!video.muted) {
      document.getElementById('muted-img').setAttribute('src', '../img/icons8-выключить-звук-48.png');
      video.muted = true;
   } else {
      document.getElementById('muted-img').setAttribute('src', '../img/icons8-громкий-звук-48.png');
      video.muted = false;
   }
}
